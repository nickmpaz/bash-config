
function time_of_day () {

  zer='
  _____/\\\\\\\____        
  ___/\\\/////\\\__       
  __/\\\____\//\\\_      
  _\/\\\_____\/\\\_     
  _\/\\\_____\/\\\_    
  _\/\\\_____\/\\\_   
  _\//\\\____/\\\__  
  __\///\\\\\\\/___ 
  ____\///////_____
  '
  one='
  ______/\\\_        
  __/\\\\\\\_       
  _\/////\\\_      
  _____\/\\\_     
  _____\/\\\_    
  _____\/\\\_   
  _____\/\\\_  
  _____\/\\\_ 
  _____\///__  
  '

  two='
  ____/\\\\\\\\\_____        
  __/\\\///////\\\___       
  _\///______\//\\\__      
  ___________/\\\/___     
  ________/\\\//_____    
  _____/\\\//________   
  ___/\\\/___________  
  __/\\\\\\\\\\\\\\\_ 
  _\///////////////__
  '
  thr='
  _____/\\\\\\\\\\__        
  ___/\\\///////\\\_       
  __\///______/\\\__      
  _________/\\\//___     
  ________\////\\\__    
  ___________\//\\\_   
  __/\\\______/\\\__  
  _\///\\\\\\\\\/___ 
  ___\/////////_____
  '
  fou='
  ____________/\\\____        
  __________/\\\\\____       
  ________/\\\/\\\____      
  ______/\\\/\/\\\____     
  ____/\\\/__\/\\\____    
  __/\\\\\\\\\\\\\\\\_   
  _\///////////\\\//__  
  ___________\/\\\____ 
  ___________\///_____
  '
  fiv='
  ___/\\\\\\\\\\\\\\\__         
  __\/\\\///////////___        
  __\/\\\______________       
  __\/\\\\\\\\\\\\_____     
  __\////////////\\\___    
  _____________\//\\\__   
  __/\\\________\/\\\__  
  _\//\\\\\\\\\\\\\/___ 
  __\/////////////_____
  '
  six='
  ____________/\\\\\_        
  ________/\\\\////__       
  _____/\\\///_______      
  ___/\\\\\\\\\\\____     
  __/\\\\///////\\\__    
  _\/\\\______\//\\\_   
  _\//\\\______/\\\__  
  __\///\\\\\\\\\/___ 
  ____\/////////_____
  '
  sev='
  __/\\\\\\\\\\\\\\\_        
  _\/////////////\\\_       
  ____________/\\\/__      
  __________/\\\/____     
  ________/\\\/______    
  ______/\\\/________   
  ____/\\\/__________  
  __/\\\/____________ 
  _\///______________
  '
  eig='
  _____/\\\\\\\\\____        
  ___/\\\///////\\\__       
  __\/\\\_____\/\\\__      
  __\///\\\\\\\\\/___     
  ___/\\\///////\\\__    
  __/\\\______\//\\\_   
  _\//\\\______/\\\__  
  __\///\\\\\\\\\/___ 
  ____\/////////_____
  '
  nin='
  ______/\\\\\\\\\____        
  ____/\\\///////\\\__       
  ___/\\\______\//\\\_      
  __\//\\\_____/\\\\\_     
  ___\///\\\\\\\\/\\\_    
  _____\////////\/\\\_   
  ___/\\________/\\\__  
  __\//\\\\\\\\\\\/___ 
  ___\///////////_____
  '
  col='
  _______      
  _______     
  __/\\\_    
  _\///__   
  _______  
  __/\\\_ 
  _\///__ 
  _______        
  _______       
  '
  numbers=($zer $one $two $thr $fou $fiv $six $sev $eig $nin)
  colon=($col)
  height=9

  time=`date +%I%M`

  echo ""
  
  #for each row
  for((i=0;i<height;i++))
  do
    #write spaces
    for ((j=0;j<i;j++))
    do
      echo -n " "
    done
    
    #left border lines
    if [ $i != 0 ]
    then    
      echo -n '\'
    else 
      echo -n " " 
    fi
    
    #write the numbers and colon
    echo -n ${numbers[$((${time:0:1} * $height + $i))]}
    echo -n ${numbers[$((${time:1:1} * $height + $i))]}
    echo -n ${colon[$i]}
    echo -n ${numbers[$((${time:2:1} * $height + $i))]}
    echo -n ${numbers[$((${time:3:1} * $height + $i))]}
    
    #right border lines
    if [ $i != 0 ]
    then
      echo -n '\'
    else
      echo -n "_"
    fi
    
    echo ""
    sleep 0.05
  done
  echo ""

}

